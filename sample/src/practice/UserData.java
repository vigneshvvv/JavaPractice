package practice;

public class UserData {
	int id;
	String name;
	String address;
	
	
	public UserData() {
		super();
	}
	
	public UserData(int id) {
		super();
		this.id = id;
		}
	
	public UserData(int id,String name) {
		super();
		this.id = id;
		this.name = name;
		}
	
	public UserData(int id, String name, String address) {
		super();
		this.id = id;
		this.name = name;
		this.address = address;}
	
	

}
